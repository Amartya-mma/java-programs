#include <iostream>
using namespace std;
int main()
{
int a,b;
cin>>a;
cin>>b;
cout<<"initially a:" <<a;
cout<<"in case of postfix increment a:"<<endl;
cout<<(a++) << endl;
cout<<"in case of prefix increment a:";
cout<<(++a) << endl;
cout<<"initially b:" <<b <<endl;
cout<<"in case of postfix decrement a:";
cout<<(b--) << endl;
cout<<"in case of prefix increment a:";
cout<<(--b) << endl;
}





